public class absmethod{
    public static void main(String[] args) {
        int num = -10;
        float num2 = 20.6f;
        double num3 = -40.5;
        int num4 = -50;

        System.out.println(Math.abs(num));
        System.out.println(Math.abs(num2));
        System.out.println(Math.abs(num3));
        System.out.println(Math.abs(num4));
    }
}